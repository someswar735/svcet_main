                                    <h4>WORKSHOP ON ADVANCED IOT + GO SOLAR</h4>
                                    <p>This is a very new inclusion of recent workshops we have designed. In this workshop, students and faculty can learn about automation, which is a highly discussed topic in the world. Companies like Apple, Microsoft, Cisco, IBM, Infosys, and TCS are working on this topic. Most of the projects will be hands-on, allowing students and learners to design their own projects through the cloud using ARDUINO and other sensors.</p>

                                    <p>Below are the session details of the workshop, which cover the most important topics:</p>
                                    <ul>
                                        <li>Interfacing solar panels with charging circuitry to charge your phone.</li>
                                        <li>Tracking sunlight and positioning the solar panel.</li>
                                        <li>Making a mobile phone charging system using solar power, which can solve all your low battery problems at any time during the day.</li>
                                        <li>Cloud Robotics: a way to control the robot from any part of the world through the cloud.</li>
                                    </ul>

